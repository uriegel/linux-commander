namespace GtkDotNet;

public enum EllipsizeMode
{
    /// <summary>
    /// No ellipsization.
    /// </summary>
    None,
    /// <summary>
    /// Omit characters at the start of the text.
    /// </summary>
    Start,
    /// <summary>
    /// Omit characters in the middle of the text.
    /// </summary>
    Middle,
    /// <summary>
    /// Omit characters at the end of the text.
    /// </summary>
    End,
}